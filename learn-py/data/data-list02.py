

a_tuple = ('crazyit', 20,5.6,'fkit',-17)

# 最后一个元素
print(a_tuple[-1])

print(a_tuple[::-1])
# 第一个元素
print(a_tuple[0])

# slice 切片: [start: end: step] start 包含， end 不包含，
# 前3个元素
print(a_tuple[0:3])

print(a_tuple[-3:-1])

# 第2个到倒数第2个
print(a_tuple[1:-2])

b_tuple = ('one','two','three','four','five','six','seven','eight','nine')

print(b_tuple[2:8:2])

# 第3个到倒数第2个
print(b_tuple[2:-2:2])

